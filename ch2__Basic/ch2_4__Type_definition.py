"""
2.4 Определение типа данных
"""

var_1 = 1  # Целое число

var_2 = 2.5  # Число с плавающей точкой

var_3 = "Hello, World"  # Строка

var_4 = True  # Логическое значение: Истина/ложь (True/False)

var_5 = [1, 2, 3, 4]  # Список

var_6 = {"Alex", "Olga", "Semen", "Nastya", "Sasha"}  # Множество

var_7 = {"Mother": "Olga", "Father": "Alex", "Son": "Sasha", "Daughter": "Nastya"}  # Словарь (ключ: значение)

print(type(var_1))  # <class 'int'>
print(type(var_2))  # <class 'float'>
print(type(var_3))  # <class 'str'>
print(type(var_4))  # <class 'bool'>
print(type(var_5))  # <class 'list'>
print(type(var_6))  # <class 'set'>
print(type(var_7))  # <class 'dict'>
